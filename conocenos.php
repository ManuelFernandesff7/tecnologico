    <div id="test1" class="col s12">
      <p class="black-text">
        La Universidad Autónoma de Nuevo León es una institución de educación que tiene como fin crear, preservar y difundir la cultura en beneficio de la sociedad de la que forma parte. Para lograrlo, desarrolla las funciones de docencia, investigación, servicio social y difusión científica y cultural que se sustentan, entre otros elementos, en una amplia infraestructura académica y administrativa integrada por bibliotecas, centros de información, laboratorios, talleres y unidades similares.
      </p>
      <p class="black-text">
        Identificando esta necesidad y basándose en la importancia que las bibliotecas tienen en la educación superior, se creó la Dirección de Bibliotecas que tiene como sede la Biblioteca Universitaria “Raúl Rangel Frías” en atención a su infraestructura, programas y servicios y para evitar la edificación de otra instancia administrativa que requiriera de grandes inversiones económicas.
      </p>
      <p class="black-text">
        Para cumplir esta responsabilidad la Dirección de Bibliotecas creó el Sistema Integral de Bibliotecas de la Universidad Autónoma de Nuevo León (SIBUANL), red que integrará a todas las bibliotecas de la Institución sin vulnerar la autonomía de cada dependencia, pues se ajustará a la normatividad vigente en cada una de ellas.
      </p>
    </div>
    <div id="test2" class="col s12">
      <p class="black-text">
        La Dirección de Bibliotecas ha propuesto la ejecución de un Plan General de Trabajo que comprende nueve programas particulares. Este plan pretende desarrollar y consolidar los servicios bibliotecarios y de información a través de la ejecución de los 9 programas de trabajo que se indican a continuación:
      </p>
      <ul class="collapsible" data-collapsible="accordion">
    <li>
      <div class="collapsible-header"><strong>1. Sistema Integral de Bibliotecas de la Universidad Autónoma de Nuevo León (SIBUANL)</strong></div>
      <div class="collapsible-body"><p><strong>Objetivo:</strong> Propiciar el desarrollo y prestación de servicios bibliotecarios y de información con calidad en la Universidad Autónoma de Nuevo León a través del mejoramiento, diversificación, ampliación y optimización de los recursos económicos, físicos, materiales y humanos que posee y de la normalización de los procesos empleando para ello las tecnologías de cómputo y telecomunicaciones de vanguardia disponibles en el mercado.</p></div>
    </li>
    <li>
      <div class="collapsible-header"><strong>2. Formación y Actualización del Personal Bibliotecario</strong></div>
      <div class="collapsible-body"><p><strong>Objetivo:</strong> Identificar las necesidades de competencia, formación y actualización del personal adscrito a las bibliotecas de la institución, para diseñar un programa de formación y actualzación que responda a los planes, funciones y actividades de cada dependencia y al mismo tiempo de los que sustente el SIBUANL en su conjunto.</p></div>
    </li>
    <li>
      <div class="collapsible-header"><strong>3. Automatización de Bibliotecas</strong></div>
      <div class="collapsible-body"><p><strong>Objetivo:</strong> Modernizar y eficientar la operación de las bibliotecas de la UANL mediante el desarrollo de una infraestructura tecnológica y de cómputo moderna y eficiente que permitan la conectividad y comunicación ínter bibliotecaria y con sus pares en México y el mundo y la prestación eficiente de modernos servicios bibliotecarios y de información.</p></div>
    </li>
     <li>
      <div class="collapsible-header"><strong>4. Biblioteca Digital UANL</strong></div>
      <div class="collapsible-body"><p><strong>Objetivo:</strong> Mejorar y eficientar los subprogramas: Bases de Datos Institucionales, Colección Digital y el Museo Virtual para que las bibliotecas de la UANL presten con eficiencia servicios electrónicos modernos y pertinentes.</p></div>
    </li>
     <li>
      <div class="collapsible-header"><strong>5. Colaboración Nacional e Internacional con Bibliotecas Digitales</strong></div>
      <div class="collapsible-body"><p><strong>Objetivo:</strong> Coadyuvar con instancias nacionales e internacionales en el desarrollo de redes de telecomunicaciones y cómputo enfocadas al desarrollo científico y educativo en Nuevo León y en el país mediante la conectividad tecnológica entre las bibliotecas de la institución y de éstas con el resto del mundo.</p></div>
    </li>
     <li>
      <div class="collapsible-header"><strong>6. Inducción a la Cultura de la Información</strong></div>
      <div class="collapsible-body"><p><strong>Objetivo:</strong> Consolidar la cultura de la información entre los directivos, profesores y alumnos de la UANL. Es decir lo relativo a las concepciones y estrategias respecto a la identificación, uso y máximo aprovechamiento de la información mediante el desarrollo de la lectura crítica, la expresión oral y escrita, los valores profesionales y la alfabetización tecnológica y de la información.</p></div>
    </li>
     <li>
      <div class="collapsible-header"><strong>7. Implantación de un sistema de administración de la calidad</strong></div>
      <div class="collapsible-body"><p><strong>Objetivo:</strong> Mejorar continuamente la operación bibliotecaria y la prestación de servicios de información en la UANL mediante la aplicación de la norma ISO 9001:2008 como estrategia para cumplir con las expectativas y necesidades de sus usuarios.</p></div>
    </li>
     <li>
      <div class="collapsible-header"><strong>8. Desarrollo de Colecciones</strong></div>
      <div class="collapsible-body"><p><strong>Objetivo:</strong> Identificar, para suplir mediante la adquisición de acervos, las necesidades de información de los directivos, profesores, investigadores, alumnos y padres de familia de la UANL.</p></div>
    </li>
     <li>
      <div class="collapsible-header"><strong>9. Mantenimiento de la Planta Física Bibliotecaria</strong></div>
      <div class="collapsible-body"><p><strong>Objetivo:</strong> Conservar en buen estado físico las instalaciones bibliotecarias de la UANL para cubrir sus necesidades operativas que garanticen la prestación de servicios de información con calidad.</p></div>
    </li>
  </ul>
    </div>
    <div id="test3" class="col s12">
      <p class="black-text">
        Nuestra política se establece sustentada en nuestra Misión, Visión y Valores, orientada en la búsqueda constante de la satisfacción con calidad de nuestros usuarios.
      </p>
      <ul class="collapsible" data-collapsible="accordion">
    <li>
      <div class="collapsible-header"><strong>Política de Calidad</strong></div>
      <div class="collapsible-body"><p class="black-text">
        En el Sistema Integral de Bibliotecas de la Universidad Autónoma de Nuevo León tenemos el compromiso de brindar servicios de calidad, basados en nuestros valores y en la mejora continua para garantizar la satisfacción de los usuarios.
      </p></div>
    </li>
    <li>
      <div class="collapsible-header"><strong>Misión</strong></div>
      <div class="collapsible-body"><p class="black-text">
        Proveer servicios bibliotecarios y de información con calidad a la comunidad universitaria y otras instituciones de Educación Media y Superior, a Nivel Nacional e Internaciona
      </p></div>
    </li>
    <li>
      <div class="collapsible-header"><strong>Visión</strong></div>
      <div class="collapsible-body"><p class="black-text">
      El SIBUANL será un modelo de red de Bibliotecas Universitarias digitales con un alto prestigio que establecerá alianzas con redes afines y con otras organizaciones Nacionales e Internacionales.</p></div>
    </li>
    <li>
      <div class="collapsible-header"><strong>Valores</strong></div>
      <div class="collapsible-body"><p class="black-text">La Dirección de Bibliotecas de la Universidad Autónoma de Nuevo León hace suyos los valores que la Universidad Autónoma de Nuevo León ha enunciado como representativos de la institución, los cuales son:</p>
      <ul class="collection">
      <li class="collection-item">Actitud de Servicio</li>
      <li class="collection-item">Ética</li>
      <li class="collection-item">Honestidad</li>
